# Nonlinear simulation of the original crane dynamics
# ------------------------------------------------------------
from scipy.integrate import solve_ivp

def nonlinear_crane_dynamics(t, state):
    # State vector
    # state[0] = xA
    # state[1] = xA_dot
    # state[2] = theta
    # state[3] = theta_dot
    # state[4] = integral of tracking error
    # state[5] = actuator first state
    # state[6] = actuator second state

    xA = state[0]
    xA_dot = state[1]
    theta = state[2]
    theta_dot = state[3]
    error_integral = state[4]
    actuator_1 = state[5]
    actuator_2 = state[6]

    # Hook position and velocity
    xB = xA + L * np.sin(theta)
    xB_dot = xA_dot + L * np.cos(theta) * theta_dot

    # Tracking error
    error = reference - xB
    error_dot = -xB_dot

    # PID controller output voltage
    voltage = Kp * error + Ki * error_integral + Kd * error_dot

    # Actuator dynamics
    actuator_1_dot = (voltage - actuator_1) / tau1
    actuator_2_dot = (actuator_1 - actuator_2) / tau2

    # Force generated by actuator
    F = Ka * actuator_2

    # Nonlinear crane equations written as a 2 by 2 system
    M = np.array([
        [mtot, m2 * L * np.cos(theta)],
        [m2 * L * np.cos(theta), m2 * L**2]
    ])

    rhs = np.array([
        F - bx * xA_dot + m2 * L * theta_dot**2 * np.sin(theta),
        -btheta * theta_dot - m2 * g * L * np.sin(theta)
    ])

    accelerations = np.linalg.solve(M, rhs)

    xA_ddot = accelerations[0]
    theta_ddot = accelerations[1]

    return [
        xA_dot,
        xA_ddot,
        theta_dot,
        theta_ddot,
        error,
        actuator_1_dot,
        actuator_2_dot
    ]


# Initial conditions
initial_state = [
    0.0,    # xA
    0.0,    # xA_dot
    0.0,    # theta
    0.0,    # theta_dot
    0.0,    # integral of error
    0.0,    # actuator first state
    0.0     # actuator second state
]

# Time span
t_start = 0.0
t_end = 300.0
t_eval = np.linspace(t_start, t_end, 3000)

solution = solve_ivp(
    nonlinear_crane_dynamics,
    [t_start, t_end],
    initial_state,
    t_eval=t_eval,
    rtol=1e-6,
    atol=1e-8
)

t_nl = solution.t
xA_nl = solution.y[0]
theta_nl = solution.y[2]
xB_nl = xA_nl + L * np.sin(theta_nl)

theta_deg = theta_nl * 180.0 / np.pi
max_swing_angle = np.max(np.abs(theta_deg))

print(f"Maximum swing angle: {max_swing_angle:.2f} deg")
print(f"Final nonlinear hook position: {xB_nl[-1]:.4f} m")
print(f"Final nonlinear trolley position: {xA_nl[-1]:.4f} m")


# Plot nonlinear xA and xB
plt.figure(figsize=(7, 4))
plt.plot(t_nl, xA_nl, label="Trolley position x_A")
plt.plot(t_nl, xB_nl, label="Hook position x_B")
plt.axhline(reference, linestyle="--", label="Reference")
plt.xlabel("Time (s)")
plt.ylabel("Position (m)")
plt.title("Nonlinear closed-loop position response")
plt.grid(True)
plt.legend()
plt.tight_layout()
plt.savefig("nonlinear_position_response.png", dpi=300)
plt.show()


# Plot nonlinear swing angle
plt.figure(figsize=(7, 4))
plt.plot(t_nl, theta_deg, label="Swing angle theta")
plt.xlabel("Time (s)")
plt.ylabel("Swing angle theta (deg)")
plt.title("Nonlinear swing angle response")
plt.grid(True)
