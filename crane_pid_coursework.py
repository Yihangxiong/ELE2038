# Robustness test for different payload masses
# ------------------------------------------------------------

def simulate_nonlinear_for_mass(payload_mass):
    m2_local = payload_mass
    mtot_local = m1 + m2_local

    def dynamics(t, state):
        xA = state[0]
        xA_dot = state[1]
        theta = state[2]
        theta_dot = state[3]
        error_integral = state[4]
        actuator_1 = state[5]
        actuator_2 = state[6]

        xB = xA + L * np.sin(theta)
        xB_dot = xA_dot + L * np.cos(theta) * theta_dot

        error = reference - xB
        error_dot = -xB_dot

        voltage = Kp * error + Ki * error_integral + Kd * error_dot

        actuator_1_dot = (voltage - actuator_1) / tau1
        actuator_2_dot = (actuator_1 - actuator_2) / tau2

        F = Ka * actuator_2

        M = np.array([
            [mtot_local, m2_local * L * np.cos(theta)],
            [m2_local * L * np.cos(theta), m2_local * L**2]
        ])

        rhs = np.array([
            F - bx * xA_dot + m2_local * L * theta_dot**2 * np.sin(theta),
            -btheta * theta_dot - m2_local * g * L * np.sin(theta)
        ])

        accelerations = np.linalg.solve(M, rhs)

        xA_ddot = accelerations[0]
        theta_ddot = accelerations[1]

        return [
            xA_dot,
            xA_ddot,
            theta_dot,
            theta_ddot,
            error,
            actuator_1_dot,
            actuator_2_dot
        ]

    solution = solve_ivp(
        dynamics,
        [t_start, t_end],
        initial_state,
        t_eval=t_eval,
        rtol=1e-6,
        atol=1e-8
    )

    t_result = solution.t
    xA_result = solution.y[0]
    theta_result = solution.y[2]
    xB_result = xA_result + L * np.sin(theta_result)

    theta_deg_result = theta_result * 180.0 / np.pi
    max_swing = np.max(np.abs(theta_deg_result))
    final_error = reference - xB_result[-1]

    maximum_value = np.max(xB_result)
    overshoot = (maximum_value - reference) / reference * 100

    tolerance = 0.02 * abs(reference)
    within_band = np.abs(xB_result - reference) <= tolerance
    outside_indices = np.where(~within_band)[0]

    if len(outside_indices) == 0:
        settling_time = 0.0
    elif outside_indices[-1] + 1 < len(t_result):
        settling_time = t_result[outside_indices[-1] + 1]
    else:
        settling_time = np.nan

    return t_result, xB_result, max_swing, final_error, overshoot, settling_time


payload_masses = [100.0, 500.0, 1000.0]
robustness_results = {}

plt.figure(figsize=(7, 4))

for mass in payload_masses:
    t_mass, xB_mass, max_swing, final_error, overshoot_mass, settling_time_mass = simulate_nonlinear_for_mass(mass)

    robustness_results[mass] = {
        "max_swing": max_swing,
        "final_error": final_error,
        "overshoot": overshoot_mass,
        "settling_time": settling_time_mass
    }

    plt.plot(t_mass, xB_mass, label=f"m2 = {mass:.0f} kg")

plt.axhline(reference, linestyle="--", label="Reference")
plt.xlabel("Time (s)")
plt.ylabel("Hook position x_B (m)")
plt.title("Robustness test for different payload masses")
plt.grid(True)
plt.legend()
plt.tight_layout()
plt.savefig("robustness_payload_mass.png", dpi=300)
plt.show()

print("\nRobustness test for different payload masses:")
for mass in payload_masses:
    result = robustness_results[mass]
    print(
        f"m2 = {mass:.0f} kg | "
        f"Overshoot = {result['overshoot']:.2f} % | "
        f"Settling time = {result['settling_time']:.2f} s | "
        f"Final error = {result['final_error']:.4f} m | "
        f"Max swing angle = {result['max_swing']:.2f} deg"
    )