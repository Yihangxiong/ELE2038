import numpy as np
import matplotlib.pyplot as plt
from scipy import signal

# System parameters
m1 = 1033.0
m2 = 500.0
L = 20.0
bx = 820.0
btheta = 2795.0
g = 9.81

Ka = 5200.0
tau1 = 0.270
tau2 = 0.010

mtot = m1 + m2

# Coefficients of the crane transfer function
a3 = m1 * m2 * L**2
a2 = bx * m2 * L**2 + btheta * mtot
a1 = m2 * g * L * mtot + bx * btheta
a0 = m2 * bx * g * L

# Crane transfer function Gc(s) = Xb(s) / F(s)
crane_num = np.array([btheta, m2 * g * L])
crane_den = np.array([a3, a2, a1, a0, 0.0])

# Actuator transfer function Ga(s) = F(s) / V(s)
actuator_num = np.array([Ka])
actuator_den = np.polymul([tau1, 1.0], [tau2, 1.0])

# Complete plant Gp(s) = Ga(s)Gc(s)
plant_num = np.polymul(actuator_num, crane_num)
plant_den = np.polymul(actuator_den, crane_den)

# Initial PID gains
Kp = 0.02
Ki = 0.0001
Kd = 0.01

# PID transfer function: Kd*s^2 + Kp*s + Ki over s
pid_num = np.array([Kd, Kp, Ki])
pid_den = np.array([1.0, 0.0])

# Open-loop transfer function
open_num = np.polymul(pid_num, plant_num)
open_den = np.polymul(pid_den, plant_den)

# Closed-loop transfer function with unity feedback
closed_num = open_num
closed_den = np.polyadd(open_den, open_num)

# Closed-loop poles
poles = np.roots(closed_den)

print("Closed-loop poles:")
print(poles)

if np.all(np.real(poles) < 0):
    print("The closed-loop system is stable.")
else:
    print("The closed-loop system is unstable.")

# Step response
reference = 20.0
t = np.linspace(0, 300, 3000)

system = signal.TransferFunction(closed_num, closed_den)
t, y_unit = signal.step(system, T=t)

y = reference * y_unit

# Metrics
maximum_value = np.max(y)
overshoot = (maximum_value - reference) / reference * 100

tolerance = 0.02 * abs(reference)
within_band = np.abs(y - reference) <= tolerance
outside_indices = np.where(~within_band)[0]

if len(outside_indices) == 0:
    settling_time = 0.0
elif outside_indices[-1] + 1 < len(t):
    settling_time = t[outside_indices[-1] + 1]
else:
    settling_time = np.nan

steady_state_error = reference - y[-1]

print(f"Overshoot: {overshoot:.2f} %")
print(f"Settling time: {settling_time:.2f} s")
print(f"Steady-state error: {steady_state_error:.4f} m")

# Plot
plt.figure(figsize=(7, 4))
plt.plot(t, y, label="Hook position x_B")
plt.axhline(reference, linestyle="--", label="Reference")
plt.xlabel("Time (s)")
plt.ylabel("Hook position x_B (m)")
plt.title("Linear closed-loop step response")
plt.grid(True)
plt.legend()
plt.tight_layout()
plt.savefig("linear_step_response.png", dpi=300)
plt.show()
